//: ## ![The Swift Logo](3DaysIcon146.png)  Type Conversion
//:
//:
//: Type Conversion is how we convert from one type of data to another.
//:
//: It's typical for initializers to accept an input. This allows us to create a value from a different value.
//:
//:
//: -------------------
//:
//: [◀  Previous Page](@previous)  |  [Next Page  ▶](@next)
//:
//: -------------------


let number: Int = 100
let double: Double = Double(number)
let string: String = String(number)
let trueString: String = String(true)
let falseString: String = String(false)

print(number)
print(double)
print(string)
print(trueString)
print(falseString)


